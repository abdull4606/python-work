# Define the radius and pi
radius = 2
pi = 3.14159

# Calculate the diameter, circumference, and area
diameter = 2 * radius
circumference = 2 * pi * radius
area = pi * radius ** 2

# Display the results
print(f"Radius: {radius}")
print(f"Diameter: {diameter}")
print(f"Circumference: {circumference}")
print(f"Area: {area}")

